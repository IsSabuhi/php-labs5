        <h2>
            <article id="Q"><b>Экология</b></article>
        </h2>
        <p>21 апреля 2011 года, Гринпис опубликовал доклад, подчеркнув тот факт, что центры обработки данных потребляют до 2 % всего мирового электричества, и эта сумма, согласно прогнозам, увеличится. Фил Рэдфорд из Гринпис сказал, что «мы обеспокоены тем,
            что этот новый взрыв в потреблении электроэнергии может загнать нас в систему старых, загрязняющих природу источников энергии вместо чистой энергии, доступной сегодня»[34]. 17 апреля 2012 года, после протеста «Гринпис» против Apple, корпорация
            выпустила заявление о том, что заканчивает использование угля и переходит к 100 % экологически чистой энергии[35][36]. В 2013 году Apple, объявила, что использует 100 % возобновляемые источники энергии для питания своих центров обработки данных,
            и в целом 75 % мощности исходит от возобновляемых источников[37]. В 2010 году Climate Counts, некоммерческая организация, занимающаяся направлением потребителей в сторону зеленых компаний, дала компании Apple 52 балла из 100 возможных, что
            ставит Apple в их высшую категорию «На пути»[38]. Это был скачок вперед по сравнению с маем 2008 года, когда Climate Counts дала Apple только 11 баллов из 100, что поместило компанию на последние место среди компаний по производству электроники,
            после чего Climate Counts также присвоила Apple «знак тугодумов», добавив, что у Apple в то время был «шанс уклониться от потребителей, переживающих за изменение климата».
        </p>
        <h2>
            <article id="L"><b>Условия труда</b></article>
        </h2>
        <p>В 2006 году Mail onSunday сообщила об условиях труда, которые существовали на заводах в Китае, где предприятия-субподрядчики Foxconn и Inventec производили iPod[57]. В статье говорится, что в одном комплексе заводов, где собирают iPod (среди прочих
            продуктов), более 200 000 рабочих жили и работали на заводе и постоянно трудились более 60 часов в неделю. В статье также сообщалось, что работники получают около $ 100 в месяц и обязаны платить компании за аренду жилья и питание, что в целом
            составляет чуть более половины заработка рабочих[58][59][60].</p>
        <p>В 2011 году Apple признала, что их поставщики в Китае используют детский труд[66].</p>
        <p><i>Рабочие на фабриках, производящих продукцию Apple, подвергаются воздействию н-гексана, нейротоксина, который является 
        более дешевой заменой этилу для очистки продуктов[67][68].</i></p>
        <p>В 2013 году China Labor Watch нашла нарушения закона и обещаний Apple об условиях труда на объектах, принадлежащих Pegatron, в том числе дискриминацию этнических меньшинств и женщин, удержание заработной платы работникам, чрезмерные рабочие часы,
            плохие условия жизни, проблемы с безопасностью и здоровьем, загрязнение окружающей среды</p>
