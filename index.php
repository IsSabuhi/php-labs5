        <p><span class="t">Apple</span> — публичная компания, её акции торгуются на бирже NASDAQ и Лондонской фондовой бирже. Общее количество выпущенных акций на начало 2011 года составляло 921,28 млн шт. По состоянию на 26 мая 2010 года рыночная стоимость
            компании (enterprise value) Apple превысила таковую у компании Microsoft[18]. По состоянию на март 2011 года рыночная капитализация компании оценивалась в $309,5 млрд[19]. В августе 2011 года Apple стала самой дорогой компанией мира по рыночной
            капитализации, которая на 10 августа составила $338,8 млрд[12]. По состоянию на 9 февраля 2012 года капитализация Apple достигла отметки в $456 млрд. Это превышает общую стоимость ближайших конкурентов Apple, Google и Microsoft, вместе взятых[20].
            В 2014 году Apple вернула лидерство по рыночной капитализации с суммой в $700 млрд.
