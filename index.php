        <p>Название фирмы происходит от англ. apple (яблоко), изображение яблока использовано в логотипе. До 9 января 2007 года официальным названием корпорации на протяжении более 30 лет было «Apple Computer, Inc.». Отказ от слова «Computer» в названии
            демонстрирует смену основного фокуса корпорации с традиционного для неё рынка компьютерной техники на рынок бытовой электроники.
            <li>Имя Apple Джобс предложил из-за того, что в этом случае телефонный номер фирмы шёл в телефонном справочнике прямо перед «Atari».</li>
            <li>«Macintosh» — сорт яблок, продающийся в США — любимый сорт яблок Джефа Раскина, который был руководителем и разработчиком проекта Macintosh перед тем, как эту должность занял Стив Джобс.</li>
        </p>
        <h2>
            <article id="C"><b>История</b></article>
        </h2>
        <hr>
        <h3><b>1970-е годы</b></h3>
        <p>Компания основана в Калифорнии Стивом Джобсом и Стивом Возняком, собравшими в середине 1970-х свой первый персональный компьютер на базе процессора «MOS Technology 6502». Продав несколько десятков таких компьютеров, молодые предприниматели получили
            финансирование и официально зарегистрировали фирму Apple Computer, Inc. 1 апреля 1976 года. «Apple I», выпущенный в 1976 году, не был первым программируемым микрокомпьютером. Право первенства принадлежало микрокомпьютеру «Альтаир 8800», который
            был создан Эдом Роберсом и распространялся через каталоги в 1974—1975 годах. Однако, «Альтаир» не был «персональным компьютером», поскольку не давал возможности решать с его помощью более или менее серьёзные задачи. В 1976—1977 годах несколькими
            фирмами были выпущены первые персональные компьютеры, в том числе с 1977 года тысячами продавались кя миллионами экземпляров, стал компьютер Apple II. С 1977 по 1993 годы фирмой Apple выпускались различные модели из линейки 8 (позднее 8/16)
            разрядных компьютеров «Apple II». В конце 1970-х и начале 1980-х годов «Apомпьютеры компаний Commodore и Tandy Radio Shack. Но первым массовым персональным компьютером, выпускавшимсple II» и их клоны были самыми распространёнными в мире персональными
            компьютерами. Было продано более 5 млн компьютеров «Apple II» по всему миру.
        </p>
        <h3><b>2010-е годы</b></h3>
        <p>В 2010 году на рынок был выпущен планшетный компьютер iPad. Производство iPod, iPhone и iPad, пользовавшихся высоким спросом по всему миру, кардинальным образом улучшило финансовое положение Apple, принося компании рекордную прибыль. В августе
            2011 года Apple впервые стала самой дорогой компанией мира по рыночной капитализации, обойдя нефтяную компанию ExxonMobil, до конца года они ещё не раз менялись местами, однако с января 2012 года Apple удалось надолго закрепиться на первой
            строчке. В августе 2012 она стала самой дорогой компанией в истории, побив установленный в декабре 1999 г., на пике т. н. пузыря доткомов, рекорд Microsoft, а 21 сентября 2012, акции Apple в ходе торгов достигли своего максимума — $705,07,
            капитализация составила $662,09 млрд.[13] Со времени максимума в сентябре 2012 года к январю 2013 года капитализация Apple сократилась на 37,6 %[13], что позволило ExxonMobil вновь соперничать с ней за первое место самой дорогой публичной
            компании мира[14]. 13 ноября 2014 года вновь Apple побила свой рекорд, её капитализация составила $663,43 млрд[15]. В 2013 году корпорация Apple первой начала серийное производство 64-битных чипов ARM-архитектуры выпустив 64-битный 2-х ядерный
            микропроцессор Apple A7[16] В 2014 году корпорация представила своё первое персональное, носимое устройство — Apple Watch.
        </p>
